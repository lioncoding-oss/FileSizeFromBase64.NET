/// Unit of measurement.
/// </summary>
public enum UnitsOfMeasurement
{
    /// <summary>
    /// B.
    /// </summary>
    Byte = 1,

    /// <summary>
    /// KB.
    /// </summary>
    KiloByte = 1_024,

    /// <summary>
    /// MB.
    /// </summary>
    MegaByte = 1_048_576
}